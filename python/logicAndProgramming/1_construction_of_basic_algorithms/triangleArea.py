#!/usr/bin/env python
"""
    his script calculates the area of ​​a triangle
    b = Base of the triangle
    h = height of the triangle
"""
b = int(input("Por favor ingrese la base del triangulo "))
h = int(input("Por favor ingrese la altura del triangulo "))

area = (b * h) / 2
print("El area del triangulo es {}".format(area))
